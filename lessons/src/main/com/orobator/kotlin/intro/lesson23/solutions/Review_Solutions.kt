package com.orobator.kotlin.intro.lesson23.solutions

// What's the difference between an Enum and a sealed class?
// You can instantiate multiple instances of a sealed class that can
// hold state. Each Enum type can only have 1 instance.


// What is a higher order function?
// A function that either takes functions as parameters or returns a function.
// Some examples are map(), apply(), fold(), filter()


// How does Kotlin handle exceptions differently than Java?
// Kotlin doesn't have checked exceptions. You aren't forced to handle
// exceptions and you don't declare them in your method signature.


// What are variance annotations and why are they used?
// (in/out)
// Used to describe a class that only produces or consumes a certain type.
// This allows us to have proper subtyping relationships.
